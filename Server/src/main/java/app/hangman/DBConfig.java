package app.hangman;

public class DBConfig {
    private String user;
    private String password;
    private int port;
    private String dbName;

    public String getUser()
    {
        return user;
    }
    public String getPassword()
    {
        return password;
    }
    public int getPort()
    {
        return port;
    }
    public String getDbName()
    {
        return dbName;
    }
}